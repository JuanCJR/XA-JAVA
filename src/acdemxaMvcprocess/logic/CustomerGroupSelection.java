	@GET
	@Path("/initialize")
	@Consumes(MediaType.APPLICATION_JSON)
	@Produces(MediaType.APPLICATION_JSON)
	public ResponseDataObject initialize(
		@QueryParam("stateVariable") CustomerGroupSelectionPDO stateVariable,
		@QueryParam("action") String action,
		@QueryParam("nextFunction") String nextFunction,
		@QueryParam("nextProgram") String nextProgram) {
		MessageObject messages[] = initArray(MessageObject.class, 20);

		try {
			assignObject(cusgrselra.stateVariable, stateVariable);
			int numRows = stateVariable.getNumRows();

			if (cusgrselra.gridVariable.length != (numRows + 1)) {
				cusgrselra.gridVariable = initArray(CustomerGroupSelectionGDO.class,
						numRows + 1);
			}

			nextFunction = "CUSGRSEL01D";
			cusgrselra.pgmInitialize();
		}
		catch (NewScreenException nse) {
			nextFunction = nse.getNextScreen();
			nextProgram = nse.getNextProgram();
		}

		assignObject(messages, cusgrselra.messages);

		retParms.setStateVariable(cusgrselra.stateVariable);
		retParms.setGridVariable(cusgrselra.gridVariable);
		retParms.setMessages(messages);

		if (messages[0].getMessageStatus() == 1) {
			return retParms;
		}

		retParms.setAction(action);
		retParms.setNextFunction(nextFunction);
		retParms.setNextProgram(nextProgram);

		return retParms;
	}

