	// stomasel01dp
	@GET
	@Path("/entryPanelProcess")
	@Consumes(MediaType.APPLICATION_JSON)
	@Produces(MediaType.APPLICATION_JSON)
	public ResponseDataObject entryPanelProcess(
		@QueryParam("stateVariable") StoreMasterSelectionPDO stateVariable,
		@QueryParam("action") String action,
		@QueryParam("nextFunction") String nextFunction,
		@QueryParam("nextProgram") String nextProgram) {
		MessageObject messages[] = initArray(MessageObject.class, 20);

		try {
			setState(stateVariable.getQin());
			setObject(stomaselra.nmfkpinds, stateVariable.getQin());
			assignObject(stomaselra.stateVariable, stateVariable);

			int numRows = stateVariable.getNumRows();

			if (stomaselra.gridVariable.length != (numRows + 1)) {
				stomaselra.gridVariable = initArray(StoreMasterSelectionGDO.class,
						numRows + 1);
			}

			stomaselra.storeMaster.lastReadEntity = new StoreMaster();
			assignObject(stomaselra.storeMaster.lastReadEntity, stateVariable, true);

			stomaselra.entryPanelProcess();
		}
		catch (NewScreenException nse) {
			nextFunction = nse.getNextScreen();
			nextProgram = nse.getNextProgram();

			if ("STOMASEL01D".equals(nextFunction.trim())) {
				nextFunction = "";
			}
		}

		assignObject(stateVariable, stomaselra.stateVariable);
		assignObject(messages, stomaselra.messages);

		retParms.setStateVariable(stateVariable);
		retParms.setGridVariable(stomaselra.gridVariable);
		retParms.setMessages(messages);

		if (messages[0].getMessageStatus() == 1) {
			return retParms;
		}

		stateVariable.setQin(objectToString(stomaselra.nmfkpinds));

		retParms.setAction(action);
		retParms.setNextFunction(nextFunction);
		retParms.setNextProgram(nextProgram);

		return retParms;
	}

